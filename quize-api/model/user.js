
// user schema

const mongoose = require('mongoose')
 
const userquizz = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
})
module.exports=mongoose.model('userquizz',userquizz );